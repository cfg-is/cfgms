func (s *SyntheticMonitoringSuite) generateAlert(severity, message, testName string) {
	alert := SyntheticAlert{
		AlertID:   fmt.Sprintf("alert-%d", time.Now().UnixNano()),
		Severity:  severity,
		Message:   message,
		TestName:  testName,
		Timestamp: time.Now(),
		Resolved:  false,
	}

	s.monitoringData.mu.Lock()
	s.monitoringData.Alerts = append(s.monitoringData.Alerts, alert)
	s.monitoringData.mu.Unlock()

	s.framework.logger.Warn("Synthetic monitoring alert generated",
		"alert_id", alert.AlertID,
		"severity", severity,
		"message", message,
		"test_name", testName)
}

func (s *SyntheticMonitoringSuite) recordTestResult(testName string, result SyntheticTestResult) {
	s.monitoringData.mu.Lock()
	if s.monitoringData.TestResults[testName] == nil {
		s.monitoringData.TestResults[testName] = make([]SyntheticTestResult, 0)
	}
	s.monitoringData.TestResults[testName] = append(s.monitoringData.TestResults[testName], result)
	s.monitoringData.LastUpdate = time.Now()
	s.monitoringData.mu.Unlock()
}

func (s *SyntheticMonitoringSuite) calculateAverageAvailability(samples []SystemMetrics) float64 {
	if len(samples) == 0 {
		return 0.0
	}

	total := 0.0
	for _, sample := range samples {
		total += sample.AvailabilityPercent
	}
	return total / float64(len(samples))
}

func (s *SyntheticMonitoringSuite) calculateAverageErrorRate(samples []SystemMetrics) float64 {
	if len(samples) == 0 {
		return 0.0
	}

	total := 0.0
	for _, sample := range samples {
		total += sample.ErrorRate
	}
	return total / float64(len(samples))
}

func (s *SyntheticMonitoringSuite) updateSystemMetrics(latest SystemMetrics) {
	s.monitoringData.mu.Lock()
	s.monitoringData.SystemMetrics = latest
	s.monitoringData.mu.Unlock()
}

func (s *SyntheticMonitoringSuite) exportMonitoringData() {
	s.monitoringData.mu.RLock()
	defer s.monitoringData.mu.RUnlock()

	// Export monitoring data as JSON for external analysis
	data, err := json.MarshalIndent(s.monitoringData, "", "  ")
	if err != nil {
		s.framework.logger.Error("Failed to marshal monitoring data", "error", err)
		return
	}

	s.framework.logger.Info("Synthetic monitoring data exported",
		"data_size_bytes", len(data),
		"test_results_count", len(s.monitoringData.TestResults),
		"alerts_count", len(s.monitoringData.Alerts))

	// In a real implementation, this would be sent to monitoring systems
	// For testing, just log that export occurred
}

